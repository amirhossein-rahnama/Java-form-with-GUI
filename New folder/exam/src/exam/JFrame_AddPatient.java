    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(
           !jTextField1.getText().trim().isEmpty()&&
           !jTextField2.getText().trim().isEmpty()&&
           !jTextField3.getText().trim().isEmpty()&&
           !jTextField4.getText().trim().isEmpty()&&
           !jTextField5.getText().trim().isEmpty()) {
            String pat_id=jTextField1.getText().trim();
            String pat_name=jTextField2.getText().trim();
            String pat_surname=jTextField3.getText().trim();
            String pat_telephone=jTextField4.getText().trim();
            String pat_address=jTextField5.getText().trim();
            

            if (!edit_Patient) {
            DentistClinic.add_Patient( pat_id,  pat_name,  pat_surname,  pat_telephone,  pat_address);
                JOptionPane.showMessageDialog(null, 
                        "The New patient Data is Recorded Successfully");
            } else {
            DentistClinic.edit_Patient(pat_id,  pat_name,  pat_surname,  pat_telephone,  pat_address);
                JOptionPane.showMessageDialog(null, 
                        "The Selected patient Data is Edited Successfully");
            }
            DentistClinic.list_Patient();
            setVisible(false);
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

