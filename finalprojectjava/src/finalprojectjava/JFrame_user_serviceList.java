    public String fixedLengthString(String str, int flength){
   String tstr="";
   for(int i=0; i<(flength-str.length()); i++) {
      tstr+=" "; 
   }
   tstr+=str;
   return tstr;
}
public void refresh_user_serviceList() {
    List user_service = user_servicesystem.user_service;
    user_service st;
    String cp;
    jList1.removeAll();
    String [] arr_user_service= new String[user_service.size()];
    cp="";
    cp+=fixedLengthString("admin code",10)+"|"+
       fixedLengthString(" admin fullname",15)+"|"+
       fixedLengthString("admin details",15);
       jLabel1.setText(cp);
       
            
    for (int i=0; i< user_service.size(); i++) {
        st=(user_service) user_service.get(i);
        arr_user_service[i]=fixedLengthString(st.getAdmin_code().trim(),10)+"|"+
                fixedLengthString(st.getAdmin_fullname().trim(),15)+"|"+
                fixedLengthString(st.getAdmin_details().trim(),15);
    }  
    jList1.setListData(arr_user_service);
}
